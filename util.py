def resToPixels(resolution):
    if resolution == 2160:
        return 8294400
    elif resolution == 1440:
        return 3686400
    elif resolution == 1080:
        return 2073600
    elif resolution == 720:
        return 921600
    elif resolution == 480:
        return 409920
    elif resolution == 360:
        return 230400
    elif resolution == 240:
        return 102240
